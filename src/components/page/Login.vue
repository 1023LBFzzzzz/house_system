<template>
    <div class="login-wrap">
        <div class="ms-login">
            <div class="ms-title">后台管理系统</div>
            <el-form :model="ruleForm" :rules="rules" ref="ruleForm" label-width="0px" class="ms-content">
                <el-form-item prop="username">
                    <el-input v-model="ruleForm.username" placeholder="username">
                        <el-button slot="prepend" icon="el-icon-lx-people"></el-button>
                    </el-input>
                </el-form-item>
                <el-form-item prop="password">
                    <el-input type="password" placeholder="password" v-model="ruleForm.password" @keyup.enter.native="submitForm('ruleForm')">
                        <el-button slot="prepend" icon="el-icon-lx-lock"></el-button>
                    </el-input>
                </el-form-item>
                <div class="login-btn">
                    <el-button type="primary" @click="submitForm('ruleForm')">登录</el-button>
                </div>
                <p class="login-tips"></p>
            </el-form>
        </div>
    </div>
</template>

<script>
    export default {
        data: function(){
            return {
                ruleForm: {
                    username: '',
                    password: ''
                },
                rules: {
                    username: [
                        { required: true, message: '请输入用户名', trigger: 'blur' }
                    ],
                    password: [
                        { required: true, message: '请输入密码', trigger: 'blur' }
                    ]
                }
            }
        },
        methods: {
            submitForm(formName) {
                this.$refs[formName].validate((valid) => {
                    if (valid) {
                        var params = {
                            loginName: this.ruleForm.username,
                            loginType: 0,
                            password: this.ruleForm.password,
                            verifyType: 0
                        }
                        this.$http.post('/neusoft/user/user/login.htm', params).then(response => {
                            var resBody = response.body
                            if (resBody.isSuccess === 0) {
                                // var url = that.$store.state.url
                                // console.log(url)
                                // that.$store.dispatch('updateUrl', '')
                                if(resBody.result!==undefined){
                                    alert("登录成功")
                                    sessionStorage.setItem('user', JSON.stringify  (resBody.result))
                                    console.log(sessionStorage.getItem("token"))
                                    this.$router.push({path: '/dashboard'})
                                }else{
                                    alert("登录失败")
                                    this.$router.go(-1)
                                }

                            } else {
                                alert({
                                    title: '操作提示',
                                    content: resBody.errorMsg
                                })
                            }
                        })
                    } else {
                        console.log('error submit!!');
                        return false;
                    }
                });
            }
        }
    }
</script>

